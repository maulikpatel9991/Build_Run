# [[1],[1,1],[1,2,1][1,3,3,1],[1,4,6,4,1]]
b = []
for i in range(1,5):
    c = [1] * i
    for j in range(1,i-1):
        c[j] = b[i-2][j-1] + b[i-2][j]
    b.append(c)
print(b)
for i in b:
    s = " ".join(map(str,i))
    print(f"{s:^10}")
for i in range(len(b)):
    s = " ".join(map(str,b[len(b) -1 - i]))
    print(f"{s:^10}")

